public class Floop {
	public static void main(String[] args){
		for(int counter =6; counter <= 21; counter+=3){//takes 3 argument inside it,first where you want it to start,second where you want it to end, how much increament it by
			System.out.println(counter); //starts at one until it hits 10 and printed it 
		}
	
	}
}